import math

# redondeo hacia abajo
resultado = math.floor(89.665)
print(resultado)

# redondeo hacia arriba
resultado = math.ceil(89.665)
print(resultado)

# PI
resultado = math.pi
print(resultado)

# exponcia al reves seri 2 ya que 5 elevado a 2 da 25
resultado = math.log(25, 5)
print(resultado)

# tangente
resultado = math.tan(2334)
print(resultado)

# coseno
resultado = math.cos(234)
print(resultado)

# factorial
resultado = math.factorial(7)
print(resultado)

