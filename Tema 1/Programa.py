# Programa cerveza normal con espacios
print("never"+" "+(input("Escribe tu color favorito en ingles:")+" "+input("Escribe tu marca de cerveza:")))

# Programa cerveza con saltos de linea
color = input("Escribe tu color favorito en ingles:")
